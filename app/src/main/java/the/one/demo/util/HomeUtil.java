import org.jsoup.Jsoup;
import org.jsoup.nodes.Document;
import org.jsoup.nodes.Element;

/**
 * @author The one
 * @date 2019/3/4 0004
 * @describe TODO
 * @email 625805189@qq.com
 * @remark
 */
public class HomeUtil {

    private static final String TAG = "HomeUtil";

    public String getHomeImage(String response) {
        Document document = Jsoup.parse(response);
        Element link = document.select("div.typo").select("div.outlink").first();
        String image = link.select("p").select("img").attr("src");
        return image;
    }

}
